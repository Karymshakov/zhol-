def match_careers(scores: dict[str, float], top_n: int = 5) -> list[dict[str, Any]]:
    code = _get_code(scores)
    top_values = _get_top_values(scores)
    thinking = "analytical" if scores.get("analytical", 0) >= scores.get("intuitive", 0) else "intuitive"

    ranked = []
    for c in CAREERS:
        score = 0
        for i, t in enumerate(c["riasec"]):
            if len(code) > 0 and code[0] == t:
                score += 3
            if len(code) > 1 and code[1] == t:
                score += 2
            if len(code) > 2 and code[2] == t:
                score += 1
        for v in c["values"]:
            if v in top_values:
                score += 2
        if c["thinking"] == thinking:
            score += 1
        ranked.append({**c, "match_score": score})

    ranked.sort(key=lambda x: x["match_score"], reverse=True)
    return ranked[:top_n]


def _get_code(scores: dict[str, float]) -> str:
    riasec = ["R", "I", "A", "S", "E", "C"]
    return "".join(sorted(riasec, key=lambda k: scores.get(k, 0), reverse=True)[:3])


def _get_top_values(scores: dict[str, float]) -> list[str]:
    value_keys = ["autonomy", "impact", "creativity", "security", "recognition", "growth", "money"]
    return [k for k, _ in sorted(
        [(k, scores.get(k, 0)) for k in value_keys],
        key=lambda x: x[1], reverse=True
    )[:3]]
